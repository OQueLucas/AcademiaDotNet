namespace AbstracaoExercicio8
{
    internal class Calculadora
    {
        public static void Somar(int n1, int n2)
        {
            Console.WriteLine(n1 + n2);
        }
        public static void Somar(double n1, double n2)
        {
            Console.WriteLine(n1 + n2);
        }
        public static void Somar(int n1, int n2, int n3)
        {
            Console.WriteLine(n1 + n2 + n3);
        }
        public static void Somar(double n1, double n2, double n3)
        {
            Console.WriteLine(n1 + n2 + n3);
        }


        public static void Subtrair(int n1, int n2)
        {
            Console.WriteLine(n1 - n2);
        }
        public static void Subtrair(double n1, double n2)
        {
            Console.WriteLine(n1 - n2);
        }
        public static void Subtrair(int n1, int n2, int n3)
        {
            Console.WriteLine(n1 - n2 - n3);
        }
        public static void Subtrair(double n1, double n2, double n3)
        {
            Console.WriteLine(n1 - n2 - n3);
        }


        public static void Multiplicar(int n1, int n2)
        {
            Console.WriteLine(n1 * n2);
        }
        public static void Multiplicar(double n1, double n2)
        {
            Console.WriteLine(n1 * n2);
        }
        public static void Multiplicar(int n1, int n2, int n3)
        {
            Console.WriteLine(n1 * n2 * n3);
        }
        public static void Multiplicar(double n1, double n2, double n3)
        {
            Console.WriteLine(n1 * n2 * n3);
        }


        public static void Dividir(int n1, int n2)
        {
            Console.WriteLine(n1 / n2);
        }
        public static void Dividir(double n1, double n2)
        {
            Console.WriteLine(n1 / n2);
        }
        public static void Dividir(int n1, int n2, int n3)
        {
            Console.WriteLine(n1 / n2 / n3);
        }
        public static void Dividir(double n1, double n2, double n3)
        {
            Console.WriteLine(n1 / n2 / n3);
        }
    }
}
